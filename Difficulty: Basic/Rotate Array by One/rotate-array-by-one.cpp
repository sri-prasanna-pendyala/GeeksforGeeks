class Solution {
  public:
    void rotate(vector<int> &arr) {
        // code here
        int num=arr[arr.size()-1];
        int temp=0;
        for(int i=arr.size()-1;i>0;i--)
        {
            temp=arr[i];
            arr[i]=arr[i-1];
            arr[i-1]=temp;
        }
        arr[0]=num;
    }
};


